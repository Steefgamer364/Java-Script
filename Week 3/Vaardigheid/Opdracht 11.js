function doMeth(x,y){
    const numberOne = x * 3;
    const numberTwo = x * 4
    return numberOne + numberTwo;
}

function doMeth2(x,y){
    const numberOne = x * 3;
    const numberTwo = x * 4
    return numberOne - numberTwo;
}

